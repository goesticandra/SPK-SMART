    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String kelamin = null;
        if(jkl.isSelected()){
            kelamin = "Laki-laki";
        }else if(jkp.isSelected()){
            kelamin = "Perempuan";
        }
        
        String statusp = null;
        if(sp1.isSelected()){
            statusp = "Belum Menikah";
        }else if(sp2.isSelected()){
            statusp = "Sudah Menikah";
        }else if(sp3.isSelected()){
            statusp = "Cerai Hidup";
        }else if(sp4.isSelected()){
            statusp = "Cerai Mati";
        }
        
        String jumlaht = null;
        if(jt1.isSelected()){
            jumlaht = "0-1";
        }else if(jt2.isSelected()){
            jumlaht = "2-3";
        }else if(jt3.isSelected()){
            jumlaht = "4-5";
        }else if(jt4.isSelected()){
            jumlaht = "Lebih dari 5";
        }
        
    String usia = null;
        if(us1.isSelected()){
            usia = "Dibawah 31";
        }else if(us2.isSelected()){
            usia = "31-45";
        }else if(us3.isSelected()){
            usia = "46-60";
        }else if(us4.isSelected()){
            usia = "Diatas 60";
        }
        
    String jenisp = null;
        if(jp1a.isSelected()){
            jenisp = "Aparatur/Pejabat Negara";
        }else if(jp1b.isSelected()){
            jenisp = "Wiraswasta";
        }else if(jp2a.isSelected()){
            jenisp = "Tenaga Pengajar";
        }else if(jp2b.isSelected()){
            jenisp = "Tenaga Kesehatan";
        }else if(jp3a.isSelected()){
            jenisp = "Pertanian/Peternakan";
        }else if(jp3b.isSelected()){
            jenisp = "Nelayan";
        }else if(jp4a.isSelected()){
            jenisp = "Belum/Tidak Bekerja";
        }else if(jp4b.isSelected()){
            jenisp = "Pensiunan";
        }
        
        String pendapatan = null;
        if(pd1.isSelected()){
            pendapatan = "Dibawah 1,5jt";
        }else if(pd2.isSelected()){
            pendapatan = "1,5 - 2,5jt";
        }else if(pd3.isSelected()){
            pendapatan = "2,6 - 3,5jt";
        }else if(pd4.isSelected()){
            pendapatan = "Diatas 3,6jt";
        }
        
        String sql ="insert into datawarga values (?,?,?,?,?,?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtKK.getText());
            stat.setString(2, txtNama.getText());
            stat.setString(3, txtAlamat.getText());
            stat.setString(4, kelamin);
            stat.setString(5, statusp);
            stat.setString(6, jumlaht);
            stat.setString(7, usia);
            stat.setString(8, jenisp);
            stat.setString(9, pendapatan);
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
            kosong();
            txtKK.requestFocus();
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null, "data gagal disimpan"+e);
        }
        datatable();    // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tblDataWargaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDataWargaMouseClicked
    int tabel = tblDataWarga.getSelectedRow();
    txtKK.setText(tblDataWarga.getValueAt(tabel, 0).toString());
    txtNama.setText(tblDataWarga.getValueAt(tabel, 1).toString());
    txtAlamat.setText(tblDataWarga.getValueAt(tabel, 2).toString());
    String a = tblDataWarga.getValueAt(tabel, 3).toString();
    String b = tblDataWarga.getValueAt(tabel, 4).toString();

    if ("Laki-laki".equals(a)){
                jkl.setSelected(true);
            }else {
                jkp.setSelected(true);
            }
    
    if ("Belum Menikah".equals(b)){
                sp1.setSelected(true);
        }else
    if ("Sudah Menikah".equals(b)){
                sp2.setSelected(true);
        }else
    if ("Cerai Hidup".equals(b)){
                sp3.setSelected(true);
        }else{
              sp4.setSelected(true);
              }
    
    // TODO add your handling code here:
    }//GEN-LAST:event_tblDataWargaMouseClicked

